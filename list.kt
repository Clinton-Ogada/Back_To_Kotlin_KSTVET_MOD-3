fun main() {
    // Create a read-only list of fruits containing "Apple," "Banana," and "Cherry."
    val fruits = listOf("Apple", "Banana", "Cherry")

    // Iterate through each element in the 'fruits' list using a for loop.
    for (fruit in fruits) {
        // For each 'fruit' in the list, print a message expressing that you like the specific fruit.
        println("I like $fruit")
    }
}
